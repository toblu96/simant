package _Model;

public class SimantInputData {

	// all Data
	private int antenna;
	private String form;
	private Double quantity;
	private Double dLambda;
	private Double direction;
	private Double amplitude;
	
	
	
	public void setAnt(int data) {
		this.antenna = data;
	}
	
	public Integer getAnt() {
		return this.antenna;
	}
	
	public void setForm(String data) {
		this.form = data;
	}
	
	public String getForm() {
		return this.form;
	}
	
	public void setQuant(Double data) {
		this.quantity = data;
	}
	
	public Double getQuant() {
		return this.quantity;
	}
	
	public void setDLambda(Double data) {
		this.dLambda = data;
	}
	
	public Double getDLambda() {
		return this.dLambda;
	}

	public void setDir(Double data) {
		this.direction = data;
	}
	
	public Double getDir() {
		return this.direction;
	}
	
	public void setAmp(Double data) {
		this.amplitude = data;
	}
	
	public Double getAmp() {
		return this.amplitude;
	}
	
	
	
}
